// conditional statement

// condtional operator
let result = 10 > 5 ? true : false;
console.log(result);

// if statemet
if (0 > 3) {
   console.log("kondisi pertama");
} else if (3 >0) {
   console.log("kondisi kedua");
} else {
   console.log("kondisi false");
}

// switch statement
let key = 4
switch (key) {
   case 1:
      console.log("key is 1");
      break;
   case 2:
      console.log("key is 2");
      break;
   case 3:
      console.log("key is 3");
      break;
   case 4:
      console.log("key is 4");
      break;               
   default:
      console.log("key is unknown");
      break;
}